package com.t99sdevelopment.jcli.dummysubpackage.exceptions;

public class InterfaceDoesNotExistException extends Exception {
	
	public InterfaceDoesNotExistException() {
		
		super();
		
	}
	
	public InterfaceDoesNotExistException(String s) {
		
		super(s);
		
	}
	
	public InterfaceDoesNotExistException(String s, Throwable throwable) {
		
		super(s, throwable);
		
	}
	
	public InterfaceDoesNotExistException(Throwable throwable) {
		
		super(throwable);
		
	}
	
	protected InterfaceDoesNotExistException(String s, Throwable throwable, boolean b, boolean b1) {
		
		super(s, throwable, b, b1);
		
	}
	
}